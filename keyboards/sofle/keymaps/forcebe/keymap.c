// TODO: Indicate win/mac mode
bool rgb_matrix_indicators_user(void) {
    // Base layer indicators
    switch (get_highest_layer(default_layer_state)) {
        // Mac
        case 0:
            rgb_matrix_set_color(0, 0, 50, 0); //green
            break;
        // Win
        case 1:
            rgb_matrix_set_color(0, 0, 0, 50); //blue
            break;
        // Error, probably
        default:
            rgb_matrix_set_color(0, 50, 0, 0); //red
    }
    // Temp layer indicators
    switch (get_highest_layer(layer_state)) {
        // Handled in base layer fn so do nothing
        case 0:
        case 1:
            break;
        // Sym
        case 2:
        case 3:
            rgb_matrix_set_color(0, 50, 50, 0); //yellow
            break;
        // Num
        case 4:
        case 5:
            rgb_matrix_set_color(0, 50, 25, 0); //orange
            break;
        // Nav
        case 6:
        case 7:
            rgb_matrix_set_color(0, 0, 50, 50); //cyan
            break;
        // Function
        case 8:
        case 9:
            rgb_matrix_set_color(0, 50, 0, 50); //violet
            break;
        // Error, probably
        default:
            rgb_matrix_set_color(0, 50, 0, 0); //red
            break;
    }
    return false;
}

